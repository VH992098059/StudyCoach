// GetCurrentTimeString 返回格式化的当前系统时间，用于注入到提示词中
func GetCurrentTimeString() string {
	now := time.Now()
	weekdays := []string{"星期日", "星期一", "星期二", "星期三", "星期四", "星期五", "星期六"}
	return fmt.Sprintf("%s %s %s，时区：%s",
		now.Format("2006-01-02"),
		weekdays[now.Weekday()],
		now.Format("15:04:05"),
		now.Location().String(),
	)
}

